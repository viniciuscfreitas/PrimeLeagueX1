package br.com.primeleague.x1.enums;

/**
 * Estados possíveis de um duelo
 */
public enum DuelState {
    
    /**
     * Duelo está sendo iniciado (contagem regressiva)
     */
    STARTING,
    
    /**
     * Duelo em andamento
     */
    IN_PROGRESS,
    
    /**
     * Duelo finalizado
     */
    ENDED;
} 